#include "generic_istream.h"

using namespace std;

namespace System
{
	namespace FileSystem
	{
		///////////////////////////////////////////////////////////////////////
		generic_istream::generic_istream(std::streambuf *streamBuf)
			: istream (NULL)
		///////////////////////////////////////////////////////////////////////
		{
			m_pStreamBuffer = streamBuf;

			init (m_pStreamBuffer);
		}

		///////////////////////////////////////////////////////////////////////
		generic_istream::~generic_istream(void)
		///////////////////////////////////////////////////////////////////////
		{
			delete m_pStreamBuffer;
		}
	}
}
